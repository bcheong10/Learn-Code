class print
{
    public static void main(String[] args)
    {   
        // println method
        System.out.println("Hello World");

        // print method
        System.out.print("This is first");
        System.out.print("This is second \n");

        // printf method
        String name = "Ben";
        System.out.printf("My name is %s", name);
    }
}
